# #{} permet de mettre des int dans des variable string
puts "On va compter le nombre d'heures de travail à THP"
puts "Travail : #{10 * 5 * 11}"#nombre d heurs travailler
puts "En minutes ça fait : #{10 * 5 * 11 * 60}" #en minute

puts "Et en secondes ?"

puts 10 * 5 * 11 * 60 * 60

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"

puts 3 + 2 < 5 - 7

puts "Ça fait combien 3 + 2 ? #{3 + 2}" #calcule 2+3
puts "Ça fait combien 5 - 7 ? #{5 - 7}" # calcule 5-7

puts "Ok, c'est faux alors !"

puts "C'est drôle ça, faisons-en plus :"

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}" #cree valeur bool la vrai
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}" #bool vrai
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}" # bool faux